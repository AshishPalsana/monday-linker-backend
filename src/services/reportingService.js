// Function to initialize CRON if user allows node-cron installation
function initCron() {
  try {
    const cron = require("node-cron");
    // 8:00 PM CST = 2:00 AM UTC (roughly, depends on DST)
    // For CST (UTC-6), it's 02:00 UTC.
    // Crontab: 0 20 * * * in CST
    // We can use timezone option if node-cron supports it
    cron.schedule("0 20 * * *", () => {
      sendDailyReport();
    }, {
      timezone: "America/Chicago"
    });
    console.log("[reportingService] Daily report CRON scheduled for 8:00 PM CST");
  } catch (e) {
    console.warn("[reportingService] node-cron not found. Automatic reporting disabled.");
  }
}

