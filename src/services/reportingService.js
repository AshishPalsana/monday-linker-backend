const prisma = require("../lib/prisma");
const { getCSTDayBounds } = require("../lib/cstTime");

/**
 * Compiles a report of all daily narratives for a given date.
 */
async function compileDailyNarratives(date = new Date()) {
  const { dayStart, dayEnd } = getCSTDayBounds(date);

  const entries = await prisma.timeEntry.findMany({
    where: {
      clockIn: { gte: dayStart, lte: dayEnd },
      status: "Complete",
      narrative: { not: null },
    },
    include: {
      technician: { select: { name: true } },
    },
    orderBy: { clockIn: "asc" },
  });

  if (entries.length === 0) return null;

  let reportHtml = `
    <h2>Daily Activity Report — ${date.toLocaleDateString('en-US')}</h2>
    <table border="1" cellpadding="8" style="border-collapse: collapse; width: 100%;">
      <thead>
        <tr style="background-color: #f2f2f2;">
          <th>Technician</th>
          <th>Work Order / Task</th>
          <th>Location</th>
          <th>Narrative</th>
          <th>Hours</th>
        </tr>
      </thead>
      <tbody>
  `;

  for (const e of entries) {
    reportHtml += `
      <tr>
        <td><strong>${e.technician.name}</strong></td>
        <td>${e.workOrderLabel || e.taskDescription || "—"}</td>
        <td>${e.jobLocation || "—"}</td>
        <td>${e.narrative}</td>
        <td style="text-align: right;">${e.hoursWorked?.toFixed(2) || "0.00"}</td>
      </tr>
    `;
  }

  reportHtml += `
      </tbody>
    </table>
    <p><em>Generated by Monday Linker Reporting Service</em></p>
  `;

  return reportHtml;
}

